#include "libft.h"

int	ft_strequ(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	if (s1 == 0 || s2 == 0)
		return (0);
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
	{
		i++;
	}
	j = (s1[i] - s2[i]);
	if (j == 0)
	{
		return (1);
	}
	return (0);
}
