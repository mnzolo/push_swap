#include "libft.h"

void	ft_putnbr(int c)
{
	if (c == -2147483648)
		return (ft_putstr("-2147483648"));
	else
	{
		if (c < 0)
		{
			ft_putchar('-');
			c = c * -1;
		}
		if (c >= 0 && c <= 9)
		{
			ft_putchar(c + '0');
		}
		if (c >= 10)
		{
			ft_putnbr(c / 10);
			ft_putnbr(c % 10);
		}
	}
}
