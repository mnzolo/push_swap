#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	*s2;

	i = 0;
	s2 = (char *)s;
	if (s2 == NULL)
	{
		return ;
	}
	while (s2[i] != '\0')
	{
		ft_putchar(s2[i]);
		i++;
	}
	ft_putchar('\n');
}
